-- ── TABLAS ──────────────────────────────────────────────────

CREATE TABLE PAIS (
    IDPAIS   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE   VARCHAR2(100) NOT NULL,
    CODIGO   VARCHAR2(10)
);

CREATE TABLE DEPARTAMENTO (
    IDDEPARTAMENTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IDPAIS         NUMBER NOT NULL,
    NOMBRE         VARCHAR2(100) NOT NULL,
    CONSTRAINT FK_DEPTO_PAIS FOREIGN KEY (IDPAIS) REFERENCES PAIS(IDPAIS)
);

CREATE TABLE CIUDAD (
    IDCIUDAD       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IDDEPARTAMENTO NUMBER NOT NULL,
    NOMBRE         VARCHAR2(100) NOT NULL,
    CONSTRAINT FK_CIUDAD_DEPTO FOREIGN KEY (IDDEPARTAMENTO) REFERENCES DEPARTAMENTO(IDDEPARTAMENTO)
);

